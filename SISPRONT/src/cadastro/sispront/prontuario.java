package cadastro.sispront;

/**
 *
 * @author Carlos Eduardo
 */
public class prontuario {
    private int codpront;
    private String nomepront;
    private String cpf;
    private int numpront;
    private String data_internacao;
    private String data_alta;
    private String corredor;
    private String prateleira;
    private String num_caixa;

    /**
     * @return the codpront
     */
    public int getCodpront() {
        return codpront;
    }

    /**
     * @param codpront the codpront to set
     */
    public void setCodpront(int codpront) {
        this.codpront = codpront;
    }

    /**
     * @return the nomepront
     */
    public String getNomepront() {
        return nomepront;
    }

    /**
     * @param nomepront the nomepront to set
     */
    public void setNomepront(String nomepront) {
        this.nomepront = nomepront;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the numpront
     */
    public int getNumpront() {
        return numpront;
    }

    /**
     * @param numpront the numpront to set
     */
    public void setNumpront(int numpront) {
        this.numpront = numpront;
    }

    /**
     * @return the data_internacao
     */
    public String getData_internacao() {
        return data_internacao;
    }

    /**
     * @param data_internacao the data_internacao to set
     */
    public void setData_internacao(String data_internacao) {
        this.data_internacao = data_internacao;
    }

    /**
     * @return the data_alta
     */
    public String getData_alta() {
        return data_alta;
    }

    /**
     * @param data_alta the data_alta to set
     */
    public void setData_alta(String data_alta) {
        this.data_alta = data_alta;
    }

    /**
     * @return the corredor
     */
    public String getCorredor() {
        return corredor;
    }

    /**
     * @param corredor the corredor to set
     */
    public void setCorredor(String corredor) {
        this.corredor = corredor;
    }

    /**
     * @return the prateleira
     */
    public String getPrateleira() {
        return prateleira;
    }

    /**
     * @param prateleira the prateleira to set
     */
    public void setPrateleira(String prateleira) {
        this.prateleira = prateleira;
    }

    /**
     * @return the num_caixa
     */
    public String getNum_caixa() {
        return num_caixa;
    }

    /**
     * @param num_caixa the num_caixa to set
     */
    public void setNum_caixa(String num_caixa) {
        this.num_caixa = num_caixa;
    }


}
