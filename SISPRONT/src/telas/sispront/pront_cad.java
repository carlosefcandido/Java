    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomepront = jTextField1.getText();
        int numpront = Integer.parseInt(jTextField2.getText());
        String cpf = jFormattedTextField1.getText();
        String data_interna = jFormattedTextField2.getText();
        String data_alta = jFormattedTextField3.getText();
        String corredor = jTextField5.getText();
        String prateleira = jTextField6.getText();
        String num_caixa = jTextField7.getText();

        if (cpf.equals("")){
            JOptionPane.showMessageDialog(null, "O campo cpf não pode estar vazio!");
        }
        if (nomepront.equals("")){
            JOptionPane.showMessageDialog(null, "O campo nome não pode estar vazio!");
        }
        if (data_interna.equals("")){
            JOptionPane.showMessageDialog(null, "O campo setor não pode estar vazio!");
        }
        if (data_alta.equals("")){
            JOptionPane.showMessageDialog(null, "O campo login não pode estar vazio!");
        }
        if (corredor.equals("")){
            JOptionPane.showMessageDialog(null, "O campo senha não pode estar vazio!");
        }

        dao c1 = new dao();
        c1.Conexao();
        prontuario p1 = new prontuario();

        p1.setNomepront(nomepront);
        p1.setCpf(cpf);
        p1.setNumpront(numpront);
        p1.setData_internacao(data_interna);
        p1.setData_alta(data_alta);
        p1.setCorredor(corredor);
        p1.setPrateleira(prateleira);
        p1.setNum_caixa(num_caixa);


        prontuariodao pd1 = new prontuariodao();

        try{
            pd1.gravar(p1);
            JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso!");
            int x = JOptionPane.showConfirmDialog(null, "Deseja cadastrar mais um funcionário?");
            if(x==0){
                jTextField1.setText("");
                jTextField2.setText("");
                jFormattedTextField1.setText("");
                jFormattedTextField2.setText("");
                jFormattedTextField3.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                jTextField7.setText("");
            }else{
                principal p2 = new principal();
                p2.setVisible(true);
                setVisible(false);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado, contate o Administrador e informe o erro! Erro: "+ e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String consultanumpront = jTextField3.getText();
        dao c1 = new dao();
        c1.Conexao();
        prontuario p1 = new prontuario();
        prontuariodao pd1 = new prontuariodao();
        
        int x;

         try{
            p1 = pd1.consulta(consultanumpront);
            jTextField4.setText(Integer.toString(p1.getCodpront()));
            jTextField1.setText(p1.getNomepront());
            jFormattedTextField1.setText(p1.getCpf());
            jTextField2.setText(Integer.toString(p1.getNumpront()));
            jFormattedTextField2.setText(p1.getData_internacao());
            jFormattedTextField3.setText(p1.getData_alta());
            jTextField5.setText(p1.getCorredor());
            jTextField6.setText(p1.getPrateleira());
            jTextField7.setText(p1.getNum_caixa());

            if((x = p1.getCodpront())==0){
                JOptionPane.showMessageDialog(null, "Prontuário não cadastrado!");
            }else{
                pd1.consulta(consultanumpront);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ocorreu um erro: "+e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

